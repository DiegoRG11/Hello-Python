# Operadores Aritmeticos

print (3 + 4)
print (3 - 4)
print (3 * 4)
print (10 % 3)
print (10 // 3)
print (2 ** 3)

print("Hola " + "Python " + "Que tal?")
print("Hola " + str(5))
print("Hola " * 5)

my_float = 2.5 * 2
print("Hola " * int(my_float))

# Operadores comparativos

print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

# Operadores Logicos

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
